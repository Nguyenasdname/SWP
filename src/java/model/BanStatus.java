package model;

/**
 *
 * @author Admin
 */
public class BanStatus {
    private int userID;
    private int adminID;

    public BanStatus() {
    }

    public BanStatus(int userID, int adminID) {
        this.userID = userID;
        this.adminID = adminID;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public int getAdminID() {
        return adminID;
    }

    public void setAdminID(int adminID) {
        this.adminID = adminID;
    }
    
    
}
